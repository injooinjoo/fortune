  /// 사주에서 나온 십성 분석 결과를 받아 TOP 3 재능 추출
  static List<SipseongTalent> getTop3Talents(Map<String, int> sipseongCounts) {
    // 십성 개수로 정렬
    final sorted = sipseongCounts.entries.toList()
      ..sort((a, b) => b.value.compareTo(a.value));

    // 상위 3개 추출
    final top3Names = sorted.take(3).map((e) => e.key).toList();

    return top3Names
        .map((name) => getTalent(name))
        .whereType<SipseongTalent>()
        .toList();
  }

