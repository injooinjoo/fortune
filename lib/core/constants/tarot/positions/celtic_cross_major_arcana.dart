  /// 카드 인덱스와 위치로 해석 가져오기
  static String? getInterpretation({
    required int cardIndex,
    required String position,
    required bool isReversed,
  }) {
    final cardMeanings = meanings[cardIndex];
    if (cardMeanings == null) return null;

    final orientation = isReversed ? 'reversed' : 'upright';
    final orientationMeanings = cardMeanings[orientation];
    if (orientationMeanings == null) return null;

    return orientationMeanings[position];
  }
