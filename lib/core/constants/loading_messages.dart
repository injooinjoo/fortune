  /// 운세 타입에 해당하는 메시지 목록 반환
  static List<String> getMessages(String? fortuneType) {
    if (fortuneType == null) return _defaultMessages;

    final category = _typeToCategory[fortuneType];
    if (category == null) return _defaultMessages;

    final categoryMessages = _categoryMessages[category];
    if (categoryMessages == null) return _defaultMessages;

    // 카테고리 메시지 + 공통 메시지 섞어서 반환
    return [...categoryMessages, ..._defaultMessages];
  }

  /// 카테고리별 메시지만 반환 (공통 메시지 제외)
  static List<String> getCategoryMessages(String? fortuneType) {
    if (fortuneType == null) return _defaultMessages;

    final category = _typeToCategory[fortuneType];
    if (category == null) return _defaultMessages;

    return _categoryMessages[category] ?? _defaultMessages;
  }
