        return politicians;
      case CelebrityCategory.actor:
        return actors;
      case CelebrityCategory.sports:
        return sportsStars;
      case CelebrityCategory.proGamer:
        return proGamers;
      case CelebrityCategory.streamer:
        return streamers;
      case CelebrityCategory.youtuber:
        return youtubers;
      case CelebrityCategory.singer:
        return singers;
      case CelebrityCategory.businessLeader:
        return businessLeaders;
    }
  }

  // Search celebrities
  static List<Celebrity> searchCelebrities({
    String? query,
    CelebrityCategory? category);
    Gender? gender)
    int? minAge)
    int? maxAge)
  }) {
    var results = allCelebrities;

    // Filter by category
    if (category != null) {
      results = getCelebritiesByCategory(category);
    }

    // Filter by search query
    if (query != null && query.isNotEmpty) {
      final lowerQuery = query.toLowerCase();
      results = results.where((celebrity) {
        return celebrity.name.toLowerCase().contains(lowerQuery) ||
               celebrity.nameEn.toLowerCase().contains(lowerQuery) ||
               (celebrity.keywords?.any((k) => k.toLowerCase().contains(lowerQuery)) ?? false) ||
               (celebrity.description?.toLowerCase().contains(lowerQuery) ?? false);
      }).toList();
    }

    // Filter by gender
    if (gender != null) {
      results = results.where((c) => c.gender == gender).toList();
    }

    // Filter by age
    if (minAge != null) {
      results = results.where((c) => c.age >= minAge).toList();
    }
    if (maxAge != null) {
      results = results.where((c) => c.age <= maxAge).toList();
    }

    return results;
  }

  // Get random celebrities
  static List<Celebrity> getRandomCelebrities(int count, {CelebrityCategory? category}) {
    final source = category != null ? getCelebritiesByCategory(category) : allCelebrities;
    final shuffled = List<Celebrity>.from(source)..shuffle();
    return shuffled.take(count).toList();
  }

  // Get celebrities with same birthday
  static List<Celebrity> getCelebritiesWithBirthday(DateTime date) {
    return allCelebrities.where((celebrity) {
      return celebrity.birthDate.month == date.month && 
             celebrity.birthDate.day == date.day;
    }).toList();
  }

