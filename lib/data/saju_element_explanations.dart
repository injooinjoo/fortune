    if (areCheongan) {
      final explanation1 = cheonganExplanations[element1];
      if (explanation1 != null && explanation1['relationships'] != null) {
        return explanation1['relationships'][element2] ?? '특별한 관계가 없습니다.';
      }
    } else {
      final explanation1 = jijiExplanations[element1];
      if (explanation1 != null && explanation1['compatibility'] != null) {
        return explanation1['compatibility'][element2] ?? '일반적인 관계입니다.';
      }
    }
    return '관계 정보를 찾을 수 없습니다.';
  }
