/** 음력 → 양력 (근사) */
export function lunarToSolar(year: number, lunarMonth: number, lunarDay: number): Date {
  const entry = LUNAR_NEW_YEAR[year];
  if (!entry) {
    throw new Error(`No lunar calendar data for year ${year}`);
  }
  const lnyMs = Date.UTC(year, entry[0], entry[1]);
  const daysOffset = Math.round((lunarMonth - 1) * SYNODIC_MONTH) + (lunarDay - 1);
  return new Date(lnyMs + daysOffset * MS_PER_DAY);
}

