/** 협록: 일간 → 지지 2개 (건록의 좌우) */
// 건록 앞뒤 지지가 협록
export function computeHyeoprok(dayStem: StemKr): [BranchKr, BranchKr] {
  const BR_ORDER: BranchKr[] = ['자','축','인','묘','진','사','오','미','신','유','술','해'];
  const geonrok = GEONROK[dayStem];
  const idx = BR_ORDER.indexOf(geonrok);
  const before = BR_ORDER[(idx - 1 + 12) % 12]!;
  const after = BR_ORDER[(idx + 1) % 12]!;
  return [before, after];
}
